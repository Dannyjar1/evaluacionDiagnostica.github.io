document.getElementById('reservacion1').addEventListener('submit', function(evento) {
    evento.preventDefault(); 
  
    var nombre = document.getElementById('nombre').value;
    var inicioFecha = document.getElementById('inicioFecha').value;
    var finFecha = document.getElementById('finFecha').value;
  
    
  
    alert('¡Gracias, ' + nombre + '! Tu reserva ha sido realizada del ' + inicioFecha + ' al ' + finFecha + '.');
  });
  
